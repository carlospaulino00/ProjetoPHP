    <h2>O QUE É WHEY PROTEIN?</h2>
    <p>O Whey Protein, que vem sendo consumido em maior quantidade e frequência<br> a cada dia, é o resultado de uma série de processos industriais que são aplicados ao leite.</p>

    <p>O Whey Protein <strong>possui um alto valor nutricional e de aminoácidos essenciais,</strong><br> principalmente os de cadeia ramificada. Além disso, sendo extraído do leite, o suplemento<br> também apresenta cálcio naturalmente.</p>

    <p>O suplemento, composto por proteínas obtidas a partir do soro do leite, é comercializado<br> em pó e pode ser tomado com água, leite ou ser incluído em diversas receitas.</p>

    <p>Por definição, apenas as proteínas do soro do leite podem ser chamadas de Whey Protein.</p>

    <p>Este produto tem como principal objetivo fornecer proteínas de alto valor biológico, que poderão<br> ser usadas no trabalho de anabolismo muscular, <strong>manutenção do tecido muscular ou recuperação<br> das fibras</strong> de praticantes de atividades físicas em geral.</p>
    
